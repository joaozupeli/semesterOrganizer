# Jogo de Sobrevivência na Ilha
# Você está preso em uma ilha deserta após um naufrágio. Para sobreviver, precisará tomar decisões certas. Escolhas erradas podem levar ao fracasso, enquanto boas escolhas aumentam suas chances de ser resgatado.

# Objetivo: Tomar decisões para sobreviver e ser resgatado com o maior nível de energia possível.

# 📍 Estrutura Geral do Jogo
# Cada escolha do jogador leva a um novo cenário, onde ele recebe mais informações sobre o ambiente e precisa decidir o que fazer a seguir. Algumas decisões levam ao sucesso e ganho de energia, enquanto outras resultam em consequências de perda de energia. O jogador deve tomar decisões para manter sua energia.

# 📌 Como Funcionaria o Sistema de Energia?
# 1️⃣ O jogador começa com 100 pontos de energia. 
# 2️⃣ Cada escolha pode aumentar ou diminuir a energia dentro de um intervalo aleatório
# 📍 Cenários e Sequência de Perguntas
# 📌 Cenário 1 – O Despertar na Ilha
# 📜 Introdução:
# "Você acorda em uma ilha deserta após um naufrágio. O sol quente bate em sua pele, e o som das ondas quebra o silêncio. Sua última lembrança é do barco afundando durante uma tempestade. Agora, você precisa tomar decisões para garantir sua sobrevivência."

# ❓ Escolha:
# 🔹 (1) Explorar a floresta em busca de recursos. 

#  🔹 (2) Caminhar pela praia e procurar sinais de vida.

# 🚨 Consequências:

# Para ambos os casos, sua energia descresce entre 20 e 40 pontos

# 📌 Cenário 2A – Explorando a Floresta (se escolheu 1)

# 📜 Descrição:
# "Você entra na floresta e logo encontra um pequeno rio de água cristalina. A sombra das árvores oferece um alívio do calor intenso"

# ❓ Escolha:
# 🔹 (1) Beber a água do rio:  

#                   📜 Descrição:Humm, voce começou a passar mal, o que deseja fazer? 

#              🔹 (1) Beber mais a água do rio

#              🔹 (2) Procurar uma fonte de água mais segura


#  🔹 (2) Procurar uma outra fonte de água 

# 🚨 Consequências:

# Se escolher (1) Beber a água do rio: A água pode estar contaminada. Você começa a se sentir mal e perde 15 a 35 pontos de energia, todas as vezes que isso ocorrer.
# Se escolher (2) Procurar outra fonte de água: Você encontra um coqueiro e consegue beber água de coco, ganhando 5 a 15 pontos de energia.
# 📌 Cenário 2B – Caminhando pela Praia (se escolheu 2 no primeiro cenário)
# 📜 Descrição:
# "Você decide caminhar pela praia, sentindo o calor do sol e a areia quente sob seus pés. Você também começa a sentir fome e cansaço."

# ❓ Escolha:
# 🔹 (1) Caçar ou Pescar
# 🔹 (2) Procurar Frutas 

# 🚨 Consequências:

# Se escolher (1) Caçar ou Pescar: Você não consegue coletar nada de proteína e consome de 15 a 35 pontos de energia
# Se escolher (2) Procurar comida: Você encontra frutas e recupera 10 a 25 pontos de energia.
# 📌 Cenário 3 – A Noite Chega
# 📜 Descrição:
# "O sol começa a se pôr, e a temperatura cai rapidamente. O vento aumenta, e você sabe que precisa se preparar para a noite. Após algum tempo, você encontra destroços do barco, incluindo madeira, cordas e pedaços de tecido."

# ❓ Escolha:
# 🔹 (1) Construir um abrigo
# 🔹 (2) Acender uma fogueira 

# 🚨 Consequências:

# Se escolher (1) Construir o abrigo: Você gasta 15 a 30 pontos de energia para construir o abrigo. Pela manhã, pescadores da ilha o encontram e decidem ajudá-lo. 
# Se escolher (2) Acender uma fogueira: Você acende a fogueira e gasta de 5 a 15 pontos de energia. Um navio distante vê a fumaça e envia uma equipe de resgate rapidamente. 
# 🔹 Resultado Final:
# Ao final do jogo, o jogador é resgatado e de acordo com a energia remanescente do jogador determina o desfecho:

# Energia acima de 70: O resgate chega e você está tão bem que dá até para dar autógrafos para os socorristas. (Final Alegre – Você foi resgatado, e até parece que esteve em um spa por dias!)


# Energia entre 30 e 69: O resgate chega, mas você está tão fraco que mal consegue fazer uma piada. (Final Irônico – Você sobreviveu, mas está mais parecendo um zumbi do que um herói.)


# Energia abaixo de 30: O resgate chega, mas ele mal consegue levantar e até acha que o socorro é só mais um pesadelo. (Final Surreal – Você foi resgatado, mas vai precisar de muitos cafés para voltar à vida normal!)

# Resumo do Jogo:
# O jogo de Sobrevivência na Ilha desafia o jogador a tomar decisões difíceis sobre como lidar com a escassez de recursos, enquanto sua energia é constantemente gerenciada. A cada escolha, a energia pode aumentar ou diminuir, afetando a capacidade de sobreviver. O objetivo é manter sua energia e tomar as decisões certas para ser resgatado com o máximo de energia possível.